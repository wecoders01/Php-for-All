<?php
// Arithmetic Operators
$num1 = 10;
$num2 = 2;

// Addition
$result = $num1 + $num2;
echo $result;
echo PHP_EOL;

// Subtraction
$result = $num1 - $num2;
echo $result;
echo PHP_EOL;

// Multiplication
$result = $num1 * $num2;
echo $result;
echo PHP_EOL;

// Division
$result = $num1 / $num2;
echo $result;
echo PHP_EOL;

// Modulus
$result = $num1 % $num2;
echo $result;
echo PHP_EOL;

// Exponentiation
$result = $num1 ** $num2;
echo $result;
echo PHP_EOL;