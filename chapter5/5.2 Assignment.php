<?php
// Assignment Operators

$num1 = 10;
$num2 = 2;

$num1 = $num1 + $num2;
echo $num1;
echo PHP_EOL;

$num1 += $num2; // $num1 = $num1 + $num2;
echo $num1;
echo PHP_EOL;

$num1 -= $num2; // $num1 = $num1 - $num2;
echo $num1;
echo PHP_EOL;

$num1 *= $num2; // $num1 = $num1 * $num2;
echo $num1;
echo PHP_EOL;

$num1 /= $num2; // $num1 = $num1 / $num2;
echo $num1;
echo PHP_EOL;

$num1 %= $num2; // $num1 = $num1 % $num2;
echo $num1;
echo PHP_EOL;
